# Automated
def print_once(a, b, c, d, e, f, g, h, i, j, k):
    print(a, b, c, d, e, f, g, h, i, j, k)


def print_twice(a, b, c, d, e, f, g, h, i, j, k):
    print(a, b, c, d, e, f, g, h, i, j, k)
    print(a, b, c, d, e, f, g, h, i, j, k)


def print_four(a, b, c, d, e, f, g, h, i, j, k):
    print_twice(a, b, c, d, e, f, g, h, i, j, k)
    print_twice(a, b, c, d, e, f, g, h, i, j, k)


